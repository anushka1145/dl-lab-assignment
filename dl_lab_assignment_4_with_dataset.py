import pandas as pd

# Load dataset from CSV file
df = pd.read_csv("news.csv")

# Convert dataframe to dictionary format
data = df.to_dict(orient="records")

# Feature columns
vocab = ["breaking", "shocking", "exclusive"]

# Class labels
classes = list(df["label"].unique())

# Total documents
total_docs = len(data)

# Count documents in each class
class_counts = {}
for c in classes:
    class_counts[c] = sum(1 for d in data if d["label"] == c)

# Prior probabilities
priors = {c: class_counts[c] / total_docs for c in classes}

print("Prior Probabilities:")
for c in priors:
    print(f"P({c}) = {priors[c]}")

# Word counts
word_counts = {c: {word: 0 for word in vocab} for c in classes}
total_words = {c: 0 for c in classes}

for d in data:
    label = d["label"]
    for word in vocab:
        word_counts[label][word] += d[word]
        total_words[label] += d[word]

# Likelihood calculation with Laplace smoothing
V = len(vocab)
likelihood = {c: {} for c in classes}

for c in classes:
    for word in vocab:
        likelihood[c][word] = (word_counts[c][word] + 1) / (total_words[c] + V)

print("\nLikelihood Values:")
for c in classes:
    print("\nClass:", c)
    for word in vocab:
        print(f"P({word}|{c}) = {likelihood[c][word]}")

# Prediction function
def predict(test_input):
    results = {}

    for c in classes:
        prob = priors[c]

        for word in vocab:
            if test_input[word] == 1:
                prob *= likelihood[c][word]

        results[c] = prob

    return max(results, key=results.get)

# Test dataset
test_data = [
    {"breaking":1, "shocking":1, "exclusive":1, "label":"Fake"},
    {"breaking":0, "shocking":0, "exclusive":1, "label":"Real"},
    {"breaking":1, "shocking":0, "exclusive":0, "label":"Fake"},
    {"breaking":0, "shocking":0, "exclusive":0, "label":"Real"}
]

# Accuracy calculation
correct = 0

print("\nTesting Data:")
for t in test_data:
    actual = t["label"]
    predicted = predict(t)

    print("Actual:", actual, "| Predicted:", predicted)

    if actual == predicted:
        correct += 1

accuracy = (correct / len(test_data)) * 100

print("\nAccuracy =", accuracy, "%")